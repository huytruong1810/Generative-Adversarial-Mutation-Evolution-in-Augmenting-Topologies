package NEAT.DataStructures;

public class GraphicPack {

    public boolean _enabled;
    public double _fgx, _fgy;
    public String _inspected;

    public GraphicPack(boolean enabled, double fgx, double fgy, String inspected) {
        _enabled = enabled;
        _fgx = fgx;
        _fgy = fgy;
        _inspected = inspected;
    }

}
